/// <summary>
/// 関数のアクセス修飾子
/// </summary>
public enum MethodAccessType
{
    Private   = 1,
    Protected = 2,
    Public    = 3,
    Internal  = 4
}

/// <summary>
/// 関数の返値の型
/// </summary>
public enum MethodDataType
{
    Void   = 1,
    Int    = 2,
    String = 3,
    Double = 4
}

/// <summary>
/// 関数の引数の型
/// </summary>
public enum MethodArgumentType
{
    Int    = 1,
    String = 2,
    Double = 3
}